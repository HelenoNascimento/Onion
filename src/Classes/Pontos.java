package Classes;

public class Pontos {
	
	
	private int cod_ponto;
	
	private int valor_ponto;

	public int getCod_ponto() {
		return cod_ponto;
	}

	public void setCod_ponto(int cod_ponto) {
		this.cod_ponto = cod_ponto;
	}

	public int getValor_ponto() {
		return valor_ponto;
	}

	public void setValor_ponto(int valor_ponto) {
		this.valor_ponto = valor_ponto;
	}
	
	
	

}
